#include <iostream>

int main () {
    int birth_year = 2009;
    
    std::cout << "\tHexadecimal" << '\n';
    std::cout << '\t' << std::hex << birth_year << '\n';
    std::cout << "\tOctal" << '\n';
    std::cout << '\t' << std::oct << birth_year << '\n';
    std::cout << "\tDecimal" << '\n';
    std::cout << '\t' << std::dec << birth_year << '\n';
    std::cout << "\tAge" << '\n';
    std::cout << '\t' << std::dec << 12 << std::endl;
    
    
}
